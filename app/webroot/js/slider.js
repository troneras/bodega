	var data = [barChartData1,barChartData2,barChartData3,barChartData4,barChartData5,barChartData6];
	var charts = [];
	function set_chart (number) {		
		var ctx = $('#canvas-'+number).get(0).getContext("2d");
		var myBar = new Chart(ctx).Bar(data[number-1], {
			responsive : true
		});
		charts[number] = myBar;
	}
	$(document).ready(function () {
		var slides  = 6;
		$('.prev').click(function (e) {
			e.preventDefault();
			var current = $(this).data('current');
			var next = current - 1;
			if (current == 1){
				next = slides; // para reiniciar el bucle
			}
			$(this).data('current',next);
			$('.next').data('current',next);
			console.log(next);
			$('div[data-slide="'+current+'"]').hide("slide", { direction: "left" }, 500,function  (argument) {
				// setTimeout(function() {
			     	$('div[data-slide="'+next+'"]').show("slide", { direction: "right" }, 500);
			     		set_chart(next);
			     	if (charts[current] != undefined){
			     		charts[current].destroy();
			     	}
			      // }, 500 );
			});
			
			

		});
		$('.next').click(function (e) {
			e.preventDefault();
			var current = $(this).data('current');			
			var next = current + 1;
			if (current == slides){
				next = 1; // para reiniciar el bucle
			}
			$(this).data('current',next);
			$('.prev').data('current',next);
			console.log(next);
			$('div[data-slide="'+current+'"]').hide("slide", { direction: "right" }, 500,function () {
				$('div[data-slide="'+next+'"]').show("slide", { direction: "left" }, 500);
				set_chart(next);
				if (charts[current] != undefined){
			    	charts[current].destroy();
			    }
			});

		});
	});
